    public String getCustomerValue() {
        String customer;

        if (!customerField.getSelectedItem().toString().isEmpty()) {
            customer = customerField.getSelectedItem().toString();
        } else {
            customer = "";
        }

        return customer;
    }

    private void fillCustomerData() {
        try {
            GeneralConfig cfg = new GeneralConfig();

            // Create list of kelompok
            customerList = cfg.getCustomer();

            // Fill ComboBox with result
            customerField.removeAllItems();
            for (i = 0; i <= customerList.size() - 1; i++) {
                customerField.addItem(customerList.get(i));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            Logger.getLogger(GeneralConfig.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public Boolean getConfirmNew() {
        return this.openInvoice;
    }

