    @Action
    public void okButtonAction() {
        if (!(customerField.getSelectedItem().toString().isEmpty())) {
            setConfirmNew(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Harap isi atau pilih customer!");
            customerField.requestFocus();
        }
    }

